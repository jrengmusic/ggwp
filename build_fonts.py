def make_transform(col, row, config, scale, baseline_y):
    """
    Returns affine transform (sx, shy, shx, sy, dx, dy) that maps
    SVG pixel coords to font units with Y-flip.
    """
    grid = config["grid"]
    col_step = grid["cell_width"] + grid["col_gap"]
    row_step = grid["cell_height"] + grid["row_gap"] + grid["label_height"]
    margin_l = grid["margin_left"]
    margin_t = grid["margin_top"]
    label_h = grid["label_height"]

    baseline_svg = baseline_y + row * row_step
    cell_left_svg = margin_l + col * col_step

    sx = scale
    shy = 0.0
    shx = 0.0
    sy = -scale
    dx = -cell_left_svg * scale
    dy = baseline_svg * scale
    return (sx, shy, shx, sy, dx, dy)


