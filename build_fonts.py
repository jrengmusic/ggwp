def _tokenize_path(d):
    """Yield (cmd, [args]) tuples from an SVG path d string."""
    token_re = re.compile(
        r"([MmZzLlHhVvCcSsQqTtAa])|([+-]?(?:\d+\.?\d*|\.\d+)(?:[eE][+-]?\d+)?)"
    )
    tokens = token_re.findall(d)
    cmd = None
    args = []
    for cmd_tok, num_tok in tokens:
        if cmd_tok:
            if cmd is not None:
                yield cmd, args
            cmd = cmd_tok
            args = []
        elif num_tok:
            args.append(float(num_tok))
    if cmd is not None:
        yield cmd, args


def _chunks(lst, n):
    for i in range(0, len(lst), n):
        yield lst[i : i + n]


def _arc_to_bezier(pen, x1, y1, rx, ry, phi, large_arc, sweep, x2, y2, tx):
    """Convert SVG arc to cubic bezier curves and draw to pen."""
    if x1 == x2 and y1 == y2:
        return
    if rx == 0 or ry == 0:
        pen.lineTo(tx(x2, y2))
        return

    phi_rad = math.radians(phi)
    cos_phi = math.cos(phi_rad)
    sin_phi = math.sin(phi_rad)

    dx = (x1 - x2) / 2
    dy = (y1 - y2) / 2
    x1p = cos_phi * dx + sin_phi * dy
    y1p = -sin_phi * dx + cos_phi * dy

    rx = abs(rx)
    ry = abs(ry)
    x1p_sq = x1p * x1p
    y1p_sq = y1p * y1p
    rx_sq = rx * rx
    ry_sq = ry * ry

    lam = x1p_sq / rx_sq + y1p_sq / ry_sq
    if lam > 1:
        s = math.sqrt(lam)
        rx *= s
        ry *= s
        rx_sq = rx * rx
        ry_sq = ry * ry

    num = max(0, rx_sq * ry_sq - rx_sq * y1p_sq - ry_sq * x1p_sq)
    den = rx_sq * y1p_sq + ry_sq * x1p_sq
    sq = math.sqrt(num / den) if den != 0 else 0
    if large_arc == sweep:
        sq = -sq

    cxp = sq * rx * y1p / ry
    cyp = -sq * ry * x1p / rx

    cx = cos_phi * cxp - sin_phi * cyp + (x1 + x2) / 2
    cy = sin_phi * cxp + cos_phi * cyp + (y1 + y2) / 2

    def angle(ux, uy, vx, vy):
        n = math.sqrt(ux * ux + uy * uy) * math.sqrt(vx * vx + vy * vy)
        if n == 0:
            return 0
        c = max(-1, min(1, (ux * vx + uy * vy) / n))
        a = math.acos(c)
        if ux * vy - uy * vx < 0:
            a = -a
        return a

    theta1 = angle(1, 0, (x1p - cxp) / rx, (y1p - cyp) / ry)
    dtheta = angle(
        (x1p - cxp) / rx, (y1p - cyp) / ry, (-x1p - cxp) / rx, (-y1p - cyp) / ry
    )

    if not sweep and dtheta > 0:
        dtheta -= 2 * math.pi
    elif sweep and dtheta < 0:
        dtheta += 2 * math.pi

    n_segs = max(1, math.ceil(abs(dtheta) / (math.pi / 2)))
    d_theta_per = dtheta / n_segs
    alpha = 4 / 3 * math.tan(d_theta_per / 4)

    theta = theta1
    for _ in range(n_segs):
        cos_t = math.cos(theta)
        sin_t = math.sin(theta)
        cos_t2 = math.cos(theta + d_theta_per)
        sin_t2 = math.sin(theta + d_theta_per)

        p1x = cx + cos_phi * (rx * cos_t) - sin_phi * (ry * sin_t)
        p1y = cy + sin_phi * (rx * cos_t) + cos_phi * (ry * sin_t)
        d1x = cos_phi * (-rx * sin_t) - sin_phi * (ry * cos_t)
        d1y = sin_phi * (-rx * sin_t) + cos_phi * (ry * cos_t)
        p2x = cx + cos_phi * (rx * cos_t2) - sin_phi * (ry * sin_t2)
        p2y = cy + sin_phi * (rx * cos_t2) + cos_phi * (ry * sin_t2)
        d2x = cos_phi * (-rx * sin_t2) - sin_phi * (ry * cos_t2)
        d2y = sin_phi * (-rx * sin_t2) + cos_phi * (ry * cos_t2)

        cp1x = p1x + alpha * d1x
        cp1y = p1y + alpha * d1y
        cp2x = p2x - alpha * d2x
        cp2y = p2y - alpha * d2y

        pen.curveTo(tx(cp1x, cp1y), tx(cp2x, cp2y), tx(p2x, p2y))
        theta += d_theta_per


def draw_svg_path_to_pen(d, pen, transform=None):
    """Parse SVG path d and draw to a fontTools pen."""

    def tx(x, y):
        if transform is None:
            return x, y
        sx, shy, shx, sy, dx, dy = transform
        return sx * x + shx * y + dx, shy * x + sy * y + dy

    cx, cy = 0.0, 0.0
    sx, sy_ = 0.0, 0.0
    last_ctrl = None
    in_contour = False

    def close_if_open():
        nonlocal in_contour
        if in_contour:
            pen.endPath()
            in_contour = False

    for cmd, args in _tokenize_path(d):
        if cmd == "M":
            close_if_open()
            pts = list(_chunks(args, 2))
            first = pts[0]
            cx, cy = first
            sx, sy_ = cx, cy
            pen.moveTo(tx(cx, cy))
            in_contour = True
            last_ctrl = None
            for p in pts[1:]:
                cx, cy = p
                pen.lineTo(tx(cx, cy))
                last_ctrl = None

        elif cmd == "m":
            close_if_open()
            pts = list(_chunks(args, 2))
            first = pts[0]
            cx += first[0]
            cy += first[1]
            sx, sy_ = cx, cy
            pen.moveTo(tx(cx, cy))
            in_contour = True
            last_ctrl = None
            for p in pts[1:]:
                cx += p[0]
                cy += p[1]
                pen.lineTo(tx(cx, cy))
                last_ctrl = None

        elif cmd in ("Z", "z"):
            if in_contour:
                pen.closePath()
                in_contour = False
            cx, cy = sx, sy_
            last_ctrl = None

        elif cmd == "L":
            for p in _chunks(args, 2):
                cx, cy = p
                pen.lineTo(tx(cx, cy))
                last_ctrl = None

        elif cmd == "l":
            for p in _chunks(args, 2):
                cx += p[0]
                cy += p[1]
                pen.lineTo(tx(cx, cy))
                last_ctrl = None

        elif cmd == "H":
            for x in args:
                cx = x
                pen.lineTo(tx(cx, cy))
                last_ctrl = None

        elif cmd == "h":
            for x in args:
                cx += x
                pen.lineTo(tx(cx, cy))
                last_ctrl = None

        elif cmd == "V":
            for y in args:
                cy = y
                pen.lineTo(tx(cx, cy))
                last_ctrl = None

        elif cmd == "v":
            for y in args:
                cy += y
                pen.lineTo(tx(cx, cy))
                last_ctrl = None

        elif cmd == "C":
            for chunk in _chunks(args, 6):
                x1, y1, x2, y2, x, y = chunk
                pen.curveTo(tx(x1, y1), tx(x2, y2), tx(x, y))
                last_ctrl = (x2, y2)
                cx, cy = x, y

        elif cmd == "c":
            for chunk in _chunks(args, 6):
                x1, y1, x2, y2, x, y = chunk
                ax1, ay1 = cx + x1, cy + y1
                ax2, ay2 = cx + x2, cy + y2
                ax, ay = cx + x, cy + y
                pen.curveTo(tx(ax1, ay1), tx(ax2, ay2), tx(ax, ay))
                last_ctrl = (ax2, ay2)
                cx, cy = ax, ay

        elif cmd == "S":
            for chunk in _chunks(args, 4):
                x2, y2, x, y = chunk
                if last_ctrl is not None:
                    x1 = 2 * cx - last_ctrl[0]
                    y1 = 2 * cy - last_ctrl[1]
                else:
                    x1, y1 = cx, cy
                pen.curveTo(tx(x1, y1), tx(x2, y2), tx(x, y))
                last_ctrl = (x2, y2)
                cx, cy = x, y

        elif cmd == "s":
            for chunk in _chunks(args, 4):
                x2, y2, x, y = chunk
                ax2, ay2 = cx + x2, cy + y2
                ax, ay = cx + x, cy + y
                if last_ctrl is not None:
                    ax1 = 2 * cx - last_ctrl[0]
                    ay1 = 2 * cy - last_ctrl[1]
                else:
                    ax1, ay1 = cx, cy
                pen.curveTo(tx(ax1, ay1), tx(ax2, ay2), tx(ax, ay))
                last_ctrl = (ax2, ay2)
                cx, cy = ax, ay

        elif cmd == "Q":
            for chunk in _chunks(args, 4):
                x1, y1, x, y = chunk
                pen.qCurveTo(tx(x1, y1), tx(x, y))
                last_ctrl = (x1, y1)
                cx, cy = x, y

        elif cmd == "q":
            for chunk in _chunks(args, 4):
                x1, y1, x, y = chunk
                ax1, ay1 = cx + x1, cy + y1
                ax, ay = cx + x, cy + y
                pen.qCurveTo(tx(ax1, ay1), tx(ax, ay))
                last_ctrl = (ax1, ay1)
                cx, cy = ax, ay

        elif cmd == "T":
            for p in _chunks(args, 2):
                x, y = p
                if last_ctrl is not None:
                    x1 = 2 * cx - last_ctrl[0]
                    y1 = 2 * cy - last_ctrl[1]
                else:
                    x1, y1 = cx, cy
                pen.qCurveTo(tx(x1, y1), tx(x, y))
                last_ctrl = (x1, y1)
                cx, cy = x, y

        elif cmd == "t":
            for p in _chunks(args, 2):
                ax, ay = cx + p[0], cy + p[1]
                if last_ctrl is not None:
                    ax1 = 2 * cx - last_ctrl[0]
                    ay1 = 2 * cy - last_ctrl[1]
                else:
                    ax1, ay1 = cx, cy
                pen.qCurveTo(tx(ax1, ay1), tx(ax, ay))
                last_ctrl = (ax1, ay1)
                cx, cy = ax, ay

        elif cmd in ("A", "a"):
            relative = cmd == "a"
            for chunk in _chunks(args, 7):
                rx, ry, x_rot, large, sweep, x, y = chunk
                if relative:
                    x, y = cx + x, cy + y
                _arc_to_bezier(
                    pen, cx, cy, rx, ry, x_rot, int(large), int(sweep), x, y, tx
                )
                last_ctrl = None
                cx, cy = x, y

    if in_contour:
        pen.endPath()


def draw_rect_to_pen(x, y, w, h, pen, transform=None):
    """Draw a rectangle as a closed contour."""

    def tx(px, py):
        if transform is None:
            return px, py
        sx, shy, shx, sy, dx, dy = transform
        return sx * px + shx * py + dx, shy * px + sy * py + dy

    pen.moveTo(tx(x, y))
    pen.lineTo(tx(x + w, y))
    pen.lineTo(tx(x + w, y + h))
    pen.lineTo(tx(x, y + h))
    pen.closePath()


