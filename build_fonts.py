def build_font(config, weight_cfg):
    """Build a single TTF from a filled glyph sheet SVG."""

    grid = config["grid"]
    sheets_dir = config.get("output", {}).get("sheets_dir", "sheets")
    fonts_dir = config.get("output", {}).get("fonts_dir", "fonts")
    os.makedirs(fonts_dir, exist_ok=True)

    svg_path = os.path.join(sheets_dir, weight_cfg["filename"])
    if not os.path.exists(svg_path):
        print(f"  ERROR: SVG not found: {svg_path}")
        return None

    font_type = config.get("font_type", "monospace")
    is_mono = font_type == "monospace"

    # read reference lines from SVG
    ref_positions = read_reference_lines(svg_path, config)

    required_refs = ["capline", "baseline"]
    for r in required_refs:
        if r not in ref_positions:
            print(f"  ERROR: Reference line '{r}' not found in {svg_path}")
            return None

    capline_y = ref_positions["capline"]
    baseline_y = ref_positions["baseline"]
    xheight_y = ref_positions.get("xheight", capline_y + (baseline_y - capline_y) * 0.4)
    descender_y = ref_positions.get(
        "descender", baseline_y + (baseline_y - capline_y) * 0.3
    )

    # derive metrics
    cap_span = baseline_y - capline_y
    if cap_span <= 0:
        print(
            f"  ERROR: Invalid reference lines (capline={capline_y}, baseline={baseline_y})"
        )
        return None

    overrides = weight_cfg.get("metrics_override", {})

    if overrides:
        capH = overrides["capH"]
        scale = capH / cap_span
        asc = overrides["asc"]
        desc = overrides["desc"]
        xH = overrides.get("xH", round((baseline_y - xheight_y) * scale))
        upm = asc + abs(desc)
    else:
        target_capH = 739
        scale = target_capH / cap_span

        capH = round((baseline_y - capline_y) * scale)

        margin_t = grid.get("margin_top", 0)
        label_h = grid.get("label_height", 0)
        cell_top_y = margin_t + label_h
        asc = round((baseline_y - cell_top_y) * scale)

        desc = -round((descender_y - baseline_y) * scale)
        xH = round((baseline_y - xheight_y) * scale)
        upm = asc + abs(desc)

    cell_w = grid["cell_width"]
    advance_w = round(cell_w * scale)

    family = config["family_name"]
    weight_name = weight_cfg["name"]
    style = weight_cfg["style"]
    ps_suffix = weight_cfg["ps_suffix"]
    weight_class = weight_cfg["weight_class"]

    full_name = f"{family} {weight_name}"
    ps_name = f"{family.replace(' ', '')}-{ps_suffix}"

    print(f"\n=== {full_name} ===")
    print(f"  UPM={upm}, asc={asc}, desc={desc}, capH={capH}, xH={xH}")
    print(f"  scale={scale:.4f}, advance_w={advance_w}")
    print(
        f"  ref lines: capline={capline_y}, xheight={xheight_y}, baseline={baseline_y}, descender={descender_y}"
    )

    # parse glyphs
    glyphs_data, char_grid = parse_svg(svg_path, config)
    print(f"  Glyphs parsed: {len(glyphs_data)}")

    all_chars = sorted(glyphs_data.keys(), key=lambda c: ord(c))
    glyph_order = [".notdef", "space"] + [f"uni{ord(c):04X}" for c in all_chars]

    cmap = {0x0020: "space"}
    for c in all_chars:
        cmap[ord(c)] = f"uni{ord(c):04X}"

    metrics = {}

    # .notdef — outlined rectangle
    pen = TTGlyphPen(None)
    m = round(advance_w * 0.06)
    im = round(advance_w * 0.18)
    pen.moveTo((m, desc))
    pen.lineTo((advance_w - m, desc))
    pen.lineTo((advance_w - m, asc))
    pen.lineTo((m, asc))
    pen.closePath()
    pen.moveTo((im, desc + im))
    pen.lineTo((im, asc - im))
    pen.lineTo((advance_w - im, asc - im))
    pen.lineTo((advance_w - im, desc + im))
    pen.closePath()
    tt_glyphs = {}
    tt_glyphs[".notdef"] = pen.glyph()
    metrics[".notdef"] = (advance_w, m)

    # space
    tt_glyphs["space"] = TTGlyphPen(None).glyph()
    metrics["space"] = (advance_w, 0)

    # for proportional fonts, pre-read all sidebearing markers
    sb_cache = {}
    if not is_mono:
        for ch, (col, row) in char_grid.items():
            sb_cache[ch] = read_sidebearing_markers(svg_path, config, col, row)

    class _FakeGlyf:
        pass

    errors = []
    for c in all_chars:
        gname = f"uni{ord(c):04X}"
        segs = glyphs_data[c]
        col, row = char_grid.get(c, (0, 0))
        xform = make_transform(col, row, config, scale, baseline_y)

        tt_pen = TTGlyphPen(None)
        cu2qu_pen = Cu2QuPen(tt_pen, max_err=1.0, reverse_direction=True)
        try:
            for kind, data in segs:
                if kind == "path":
                    draw_svg_path_to_pen(data, cu2qu_pen, transform=xform)
                elif kind == "rect":
                    draw_rect_to_pen(*data, cu2qu_pen, transform=xform)
            g = tt_pen.glyph()
            if g.numberOfContours != 0:
                g.recalcBounds(_FakeGlyf())
                lsb = g.xMin
            else:
                lsb = 0

            # determine advance width
            if is_mono:
                glyph_advance = advance_w
            else:
                lsb_x, rsb_x = sb_cache.get(c, (None, None))
                if lsb_x is not None and rsb_x is not None:
                    glyph_advance = round((rsb_x - lsb_x) * scale)
                else:
                    # fallback: use bounding box + default sidebearing
                    glyph_advance = advance_w

            tt_glyphs[gname] = g
            metrics[gname] = (glyph_advance, lsb)
        except Exception as e:
            errors.append((c, str(e)))
            tt_glyphs[gname] = TTGlyphPen(None).glyph()
            metrics[gname] = (advance_w, 0)

    if errors:
        print(f"  Errors on {len(errors)} glyphs: {[e[0] for e in errors]}")

    # assemble font
    fb = FontBuilder(upm, isTTF=True)
    fb.setupGlyphOrder(glyph_order)
    fb.setupCharacterMap(cmap)
    fb.setupGlyf(tt_glyphs)
    fb.setupHorizontalMetrics(metrics)
    fb.setupHorizontalHeader(ascent=asc, descent=desc)

    version = config.get("version", "1.0")
    copyright_str = config.get("copyright", "")
    trademark = config.get("trademark", "")
    vendor = config.get("vendor", "    ")[:4].ljust(4)
    designer = config.get("designer", "")
    description = config.get("description", "")
    url = config.get("url", "")

    fb.setupNameTable(
        {
            "copyright": copyright_str,
            "familyName": full_name,
            "styleName": style,
            "uniqueFontIdentifier": f"{version};{vendor.strip()};{ps_name}",
            "fullName": full_name,
            "version": f"Version {version}",
            "psName": ps_name,
            "trademark": trademark,
            "manufacturer": vendor.strip(),
            "designer": designer,
            "description": description,
            "vendorURL": url,
            "designerURL": url,
            "licenseDescription": copyright_str,
            "licenseInfoURL": url,
            "typographicFamily": family,
            "typographicSubfamily": style,
        }
    )

    # fsSelection: bit 7 = USE_TYPO_METRICS, bit 5 = Bold
    fs_sel = 0x80
    if style == "Bold":
        fs_sel |= 0x20

    panose = Panose()
    panose.bFamilyType = 2
    panose.bSerifStyle = 0
    panose.bWeight = max(2, min(9, weight_class // 100))
    panose.bProportion = 9 if is_mono else 0
    panose.bContrast = 0
    panose.bStrokeVariation = 0
    panose.bArmStyle = 0
    panose.bLetterForm = 0
    panose.bMidline = 0
    panose.bXHeight = 0

    mac_style = 0x01 if style == "Bold" else 0x00

    fb.setupOS2(
        version=4,
        sTypoAscender=asc,
        sTypoDescender=desc,
        sTypoLineGap=0,
        usWinAscent=asc,
        usWinDescent=abs(desc),
        sxHeight=xH,
        sCapHeight=capH,
        fsType=0,
        fsSelection=fs_sel,
        achVendID=vendor,
        usWeightClass=weight_class,
        panose=panose,
    )

    fb.setupHead(unitsPerEm=upm, macStyle=mac_style)
    fb.setupPost(isFixedPitch=1 if is_mono else 0)

    out_filename = f"{ps_name}.ttf"
    out_path = os.path.join(fonts_dir, out_filename)
    fb.font.save(out_path)
    print(f"  Saved: {out_path}")
    return out_path


