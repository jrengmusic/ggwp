def _normalize_color(color_str):
    """
    Normalize a color string to lowercase hex (#rrggbb).
    Handles: #hex, rgb(r,g,b), and inline style containing stroke:rgb().
    """
    if not color_str:
        return ""
    s = color_str.strip().lower()

    m = re.search(r"rgb\(\s*(\d+)\s*,\s*(\d+)\s*,\s*(\d+)\s*\)", s)
    if m:
        r, g, b = int(m.group(1)), int(m.group(2)), int(m.group(3))
        return f"#{r:02x}{g:02x}{b:02x}"

    if s.startswith("#"):
        return s

    return s


def _get_stroke_color(elem):
    """
    Extract stroke color from an SVG element.
    Checks both stroke= attribute and style= inline style.
    Returns normalized hex color string.
    """
    stroke = elem.get("stroke", "")
    if stroke:
        return _normalize_color(stroke)

    style = elem.get("style", "")
    if "stroke" in style:
        return _normalize_color(style)

    return ""


def read_reference_lines(svg_path, config):
    """
    Read reference line Y positions from the SVG by matching line colors.
    Returns dict: {"capline": y, "xheight": y, "baseline": y, "descender": y}
    Only reads from the first row (row 0) for consistency.
    """
    tree = ET.parse(svg_path)
    root = tree.getroot()

    ref_colors = {}
    for key, ref in config["reference_lines"].items():
        ref_colors[_normalize_color(ref["color"])] = key

    grid = config["grid"]
    margin_t = grid["margin_top"]
    label_h = grid.get("label_height", 0)
    cell_h = grid["cell_height"]

    # first cell drawing area bounds (row 0)
    dy0 = margin_t
    dy1 = margin_t + cell_h + label_h + 10  # generous bounds

    results = {}
    for elem in root.iter():
        tag = elem.tag.split("}")[-1]

        if tag == "line":
            stroke = _get_stroke_color(elem)
            if not stroke or stroke not in ref_colors:
                continue

            y1 = float(elem.get("y1", "0"))
            y2 = float(elem.get("y2", "0"))

            if abs(y1 - y2) > 0.5:
                continue
            if y1 < dy0 - 1 or y1 > dy1 + 1:
                continue

            key = ref_colors[stroke]
            if key not in results:
                results[key] = y1

        elif tag == "path":
            # Affinity exports lines as path: M x1,y1 L x2,y2
            stroke = _get_stroke_color(elem)
            if not stroke or stroke not in ref_colors:
                continue

            d = elem.get("d", "")
            m = re.match(
                r"M\s*([+-]?[\d.]+)\s*[,\s]\s*([+-]?[\d.]+)\s*"
                r"L\s*([+-]?[\d.]+)\s*[,\s]\s*([+-]?[\d.]+)\s*$",
                d.strip(),
            )
            if not m:
                continue

            y1 = float(m.group(2))
            y2 = float(m.group(4))

            if abs(y1 - y2) > 0.5:
                continue
            if y1 < dy0 - 1 or y1 > dy1 + 1:
                continue

            key = ref_colors[stroke]
            if key not in results:
                results[key] = y1

    return results


def read_sidebearing_markers(svg_path, config, col, row):
    """
    Read LSB/RSB marker X positions for a specific cell.
    Returns (lsb_x, rsb_x) or (None, None) if not found.
    """
    sb = config.get("sidebearing_markers", {})
    lsb_color = _normalize_color(sb.get("lsb_color", "#00FFFF"))
    rsb_color = _normalize_color(sb.get("rsb_color", "#FF00FF"))

    grid = config["grid"]
    margin_l = grid["margin_left"]
    margin_t = grid["margin_top"]
    label_h = grid.get("label_height", 0)
    cell_w = grid["cell_width"]
    cell_h = grid["cell_height"]
    col_gap = grid["col_gap"]
    row_gap = grid["row_gap"]
    col_step = cell_w + col_gap
    row_step = cell_h + row_gap + label_h

    cell_x = margin_l + col * col_step
    cell_dy = margin_t + row * row_step + label_h

    tree = ET.parse(svg_path)
    root = tree.getroot()

    lsb_x = None
    rsb_x = None

    for elem in root.iter():
        tag = elem.tag.split("}")[-1]
        if tag != "line":
            continue

        stroke = _get_stroke_color(elem)
        if stroke not in (lsb_color, rsb_color):
            continue

        x1 = float(elem.get("x1", "0"))
        y1 = float(elem.get("y1", "0"))
        x2 = float(elem.get("x2", "0"))
        y2 = float(elem.get("y2", "0"))

        # vertical line check (x1 == x2) within cell bounds
        if abs(x1 - x2) > 0.5:
            continue
        if y1 < cell_dy - 1 or y2 > cell_dy + cell_h + 1:
            continue
        if x1 < cell_x - 1 or x1 > cell_x + cell_w + 1:
            continue

        if stroke == lsb_color:
            lsb_x = x1
        elif stroke == rsb_color:
            rsb_x = x1

    return lsb_x, rsb_x


