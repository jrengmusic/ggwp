def parse_svg(svg_path, config):
    """
    Parse a filled glyph sheet SVG.
    Returns:
        glyphs: dict char -> list of (kind, data)
        char_grid: dict char -> (col, row)
    """
    tree = ET.parse(svg_path)
    root = tree.getroot()

    grid = config["grid"]
    columns = grid["columns"]
    cell_w = grid["cell_width"]
    cell_h = grid["cell_height"]
    col_gap = grid["col_gap"]
    row_gap = grid["row_gap"]
    label_h = grid["label_height"]
    margin_l = grid["margin_left"]
    margin_t = grid["margin_top"]
    col_step = cell_w + col_gap
    row_step = cell_h + row_gap + label_h

    label_font_size = config.get("appearance", {}).get("label_font_size", 7)

    # label position for col 0, row 0
    label_x0 = margin_l + 2
    label_y0 = margin_t + label_h - 3

    # find text labels to build grid mapping
    children = list(root)
    char_grid = {}
    glyphs = {}

    for i, child in enumerate(children):
        tag = child.tag.split("}")[-1]
        if tag != "text":
            continue

        style = child.get("style", "")
        if f"font-size:{label_font_size}px" not in style or not child.text:
            continue

        ch = child.text
        # unescape XML entities
        if ch.startswith("U+") and len(ch) >= 6:
            # U+XXXX format — skip, this is a non-printable label
            try:
                cp = int(ch[2:], 16)
                ch = chr(cp)
            except ValueError:
                continue

        lx = float(child.get("x", "0").replace("px", ""))
        ly = float(child.get("y", "0").replace("px", ""))

        col = round((lx - label_x0) / col_step)
        row = round((ly - label_y0) / row_step)

        if col < 0 or row < 0:
            continue

        char_grid[ch] = (col, row)

        # cell bounding box (generous margin for stray path filtering)
        cell_x0 = margin_l + col * col_step - col_step * 0.1
        cell_x1 = margin_l + col * col_step + col_step * 1.1
        cell_dy = margin_t + row * row_step + label_h
        cell_y0 = cell_dy - row_step * 0.1
        cell_y1 = cell_dy + cell_h + row_step * 0.1

        def _first_coord(d):
            m = re.search(r"[Mm]\s*([+-]?[\d.]+)[,\s]+([+-]?[\d.]+)", d)
            if m:
                return float(m.group(1)), float(m.group(2))
            return None, None

        # find the next <g> after this label
        for j in range(i + 1, min(i + 15, len(children))):
            c = children[j]
            if c.tag.split("}")[-1] == "g":
                segs = []
                for sub in c:
                    subtag = sub.tag.split("}")[-1]
                    if subtag == "path":
                        d = sub.get("d", "")
                        if d:
                            px, py = _first_coord(d)
                            if px is not None and not (
                                cell_x0 <= px <= cell_x1 and cell_y0 <= py <= cell_y1
                            ):
                                continue
                            segs.append(("path", d))
                    elif subtag == "rect":
                        rx = float(sub.get("x", 0))
                        ry = float(sub.get("y", 0))
                        rw = float(sub.get("width", 0))
                        rh = float(sub.get("height", 0))
                        # skip cell border rects (no fill, stroke only)
                        fill = sub.get("fill", "")
                        if fill == "none":
                            continue
                        segs.append(("rect", (rx, ry, rw, rh)))
                if segs:
                    glyphs[ch] = segs
                break

    return glyphs, char_grid


